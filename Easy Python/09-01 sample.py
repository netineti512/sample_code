#文字列の操作を知る
str = input("文字列を入力して下さい。\n")

print("文字列は", str, "です。")
print("0番目の文字は", str[0], "です。")    #インデックスが0の文字を取得します。
print("文字列を逆順にすると", str[::-1], "です。")
print("文字列の長さは", len(str), "です。")     #文字列の長さを取得します。


#>>njscjdcikj[
