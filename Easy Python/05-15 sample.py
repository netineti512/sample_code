#リストの集計
sale = [80, 60, 22, 50, 75]
print("現在のデータは", sale, "です。")

#リストの集計を行います。
print("最大のデータは", max(sale), "です。")
print("最小のデータは", min(sale), "です。")
print("データの合計は", sum(sale), "です。")

print("ソートされたデータは", sorted(sale), "です。")


#現在のデータは [80, 60, 22, 50, 75] です。
#最大のデータは 80 です。
#最小のデータは 22 です。
#データの合計は 287 です。
#ソートされたデータは [22, 50, 60, 75, 80] です。